
/**
 * skillTaxonomy.ts
 *
 * OptioHire — Master Skill Taxonomy (100 Professional Categories)
 *
 * This file is intentionally NOT injected into the LLM prompt in full.
 * It lives in Node.js memory and is queried by the JIT Taxonomy Router
 * (getRelevantTaxonomyCategories) at runtime, which selects only the
 * 2-4 most relevant categories for a given job posting before
 * constructing the AI prompt. See ai-scoring.ts for the consumer.
 *
 * Each category has:
 *  - id: stable machine key (snake_case)
 *  - label: human-readable name
 *  - keywords: title/skill tokens used by the router for fast matching
 *  - skills: 8-10 representative hard/domain skills the AI checks
 *            candidate CVs against for that profession
 *
 * UNIVERSAL_SOFT_SKILLS is a separate add-on layer, always available to
 * the router to optionally append regardless of which category(ies)
 * matched — it is NOT one of the 100 numbered categories.
 */

export interface TaxonomyCategory {
    id: string;
    label: string;
    group: string;
    keywords: string[];
    skills: string[];
}

