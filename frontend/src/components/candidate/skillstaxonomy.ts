/**
 * Builds a quick lookup index once at module load time so the router
 * doesn't re-scan the full array on every request.
 */
const TAXONOMY_INDEX: { category: TaxonomyCategory; keyword: string }[] = [];
for (const category of SKILL_TAXONOMY) {
    for (const keyword of category.keywords) {
        TAXONOMY_INDEX.push({ category, keyword: keyword.toLowerCase() });
    }
}

export interface RoutedTaxonomyResult {
    categories: TaxonomyCategory[];
    matchedBy: "keyword" | "fallback_generic";
}

/**
 * getRelevantTaxonomyCategories
 *
 * Fast, local, pre-LLM router. Given a job title and a list of
 * required skill strings, returns the 2-4 most relevant taxonomy
 * categories (NOT the AI prompt itself — this never touches the
 * model). The caller (ai-scoring.ts) is responsible for serializing
 * the returned skills into a compact prompt fragment.
 *
 * Matching strategy:
 *  1. Normalize jobTitle + requiredSkills into a single lowercase blob.
 *  2. Score every category by counting keyword hits in that blob.
 *  3. Return the top N categories by score (N = maxCategories).
 *  4. If nothing matches (score 0 everywhere), fall back to a small
 *     generic set so the AI still has *something* to anchor against
 *     rather than an empty taxonomy.
 *
 * @param jobTitle        e.g. "Senior Backend Engineer"
 * @param requiredSkills  e.g. ["Node.js", "PostgreSQL", "AWS"]
 * @param maxCategories   ceiling on how many categories to inject (default 3)
 */
export function getRelevantTaxonomyCategories(
    jobTitle: string,
    requiredSkills: string[] = [],
    maxCategories: number = 3
): RoutedTaxonomyResult {
    const blob = `${jobTitle} ${requiredSkills.join(" ")}`.toLowerCase();

    const scores = new Map<string, number>();
    for (const { category, keyword } of TAXONOMY_INDEX) {
        if (keyword && blob.includes(keyword)) {
            scores.set(category.id, (scores.get(category.id) ?? 0) + 1);
        }
    }

    if (scores.size === 0) {
        // Fallback: no keyword hit at all. Give the AI a small, broadly
        // applicable set rather than nothing. We deliberately keep this
        // tiny — it's a safety net, not a substitute for good keywords.
        const fallbackIds = [
            "operations_management_coo",
            "customer_support_ops",
            "executive_admin_assistance",
        ];
        return {
            categories: SKILL_TAXONOMY.filter((c) => fallbackIds.includes(c.id)),
            matchedBy: "fallback_generic",
        };
    }

    const ranked = Array.from(scores.entries())
        .sort((a, b) => b[1] - a[1])
        .slice(0, maxCategories)
        .map(([id]) => id);

    return {
        categories: SKILL_TAXONOMY.filter((c) => ranked.includes(c.id)),
        matchedBy: "keyword",
    };
}

/**
 * serializeTaxonomyForPrompt
 *
 * Converts the routed categories (+ optional universal soft-skills
 * layer) into a compact text block for injection into the AI prompt.
 * This is what keeps token usage low — only ~100-200 words instead
 * of the full ~5,000-word taxonomy.
 *
 * @param categories          Output of getRelevantTaxonomyCategories().categories
 * @param includeSoftSkills   Whether to append the universal layer (default true)
 */
export function serializeTaxonomyForPrompt(
    categories: TaxonomyCategory[],
    includeSoftSkills: boolean = true
): string {
    const blocks = categories.map(
        (c) => `${c.label}:\n- ${c.skills.join("\n- ")}`
    );

    if (includeSoftSkills) {
        blocks.push(
            `${UNIVERSAL_SOFT_SKILLS.label}:\n- ${UNIVERSAL_SOFT_SKILLS.skills.join("\n- ")}`
        );
    }

    return blocks.join("\n\n");
}

/**
 * getCategoryById — convenience lookup, e.g. for admin UI / debugging.
 */
export function getCategoryById(id: string): TaxonomyCategory | undefined {
    return SKILL_TAXONOMY.find((c) => c.id === id);
}

